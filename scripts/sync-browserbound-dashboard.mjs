import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const origin = 'https://www.browserbound.com';

function extractDiv(html, startNeedle) {
	const start = html.indexOf(startNeedle);
	if (start < 0) throw new Error(`Cannot find ${startNeedle}`);
	const tagRe = /<\/?div\b[^>]*>/gi;
	tagRe.lastIndex = start;
	let depth = 0;
	let match;
	while ((match = tagRe.exec(html))) {
		if (match[0].startsWith('</')) depth -= 1;
		else depth += 1;
		if (depth === 0) return html.slice(start, tagRe.lastIndex);
	}
	throw new Error(`Cannot close ${startNeedle}`);
}

function normalizeSnapshot(html) {
	return html
		.replaceAll('src="/icons/', `src="${origin}/icons/`)
		.replaceAll('href="/dashboard"', 'href="/"')
		.replaceAll('href="/pdf-tools/merge"', 'href="/pdf-tools/merge/"')
		.replaceAll('href="/pdf-tools/split"', 'href="/pdf-tools/split/"')
		.replaceAll('href="/pdf-tools/compress"', 'href="/pdf-tools/compress/"')
		.replaceAll('href="/pdf-tools/pdf-to-word"', 'href="/pdf-tools/pdf-to-word/"')
		.replaceAll('href="/pdf-tools/word-to-pdf"', 'href="/pdf-tools/word-to-pdf/"')
		.replaceAll('href="/pdf-tools/ocr"', 'href="/pdf-tools/ocr/"')
		.replaceAll('href="/pdf-tools/watermark"', 'href="/pdf-tools/watermark/"')
		.replaceAll('href="/pdf-tools/unlock"', 'href="/pdf-tools/unlock/"')
		.replaceAll('href="/pdf-tools/protect"', 'href="/pdf-tools/protect/"')
		.replaceAll('href="/pdf-tools/rotate"', 'href="/pdf-tools/rotate/"');
}

async function main() {
	const dashboardHtml = await fetch(`${origin}/dashboard`, {
		headers: { 'user-agent': 'PDFKING dashboard sync' },
	}).then((response) => {
		if (!response.ok) throw new Error(`Dashboard fetch failed: ${response.status}`);
		return response.text();
	});

	const wrapper = normalizeSnapshot(extractDiv(dashboardHtml, '<div data-slot="sidebar-wrapper"'));
	const stylesheetLinks = [...dashboardHtml.matchAll(/<link rel="stylesheet" href="([^"]+)"/g)].map((match) => new URL(match[1], origin).href);
	const cssParts = [];
	for (const url of stylesheetLinks) {
		if (!url.includes('/_next/static/chunks/')) continue;
		const css = await fetch(url).then((response) => {
			if (!response.ok) throw new Error(`CSS fetch failed: ${response.status} ${url}`);
			return response.text();
		});
		// Keep utility/theme CSS local. Font CSS uses relative Next media URLs, so PDFKING supplies the font separately.
		if (css.includes('--background') || css.includes('.bg-background')) cssParts.push(css);
	}

	await mkdir(path.join(root, 'src', 'data'), { recursive: true });
	await mkdir(path.join(root, 'src', 'styles'), { recursive: true });
	await writeFile(
		path.join(root, 'src', 'data', 'browserbound-dashboard-snapshot.ts'),
		[
			'// Generated by scripts/sync-browserbound-dashboard.mjs',
			'// Authorized BrowserBound dashboard snapshot used for PDFKING design-language parity.',
			`export const browserboundDashboardHtml = ${JSON.stringify(wrapper)};`,
			'',
		].join('\n'),
	);
	await writeFile(
		path.join(root, 'src', 'styles', 'browserbound-dashboard.css'),
		[
			'/* Generated by scripts/sync-browserbound-dashboard.mjs */',
			'/* Authorized BrowserBound dashboard utility/theme CSS used for PDFKING design-language parity. */',
			...cssParts,
			'',
		].join('\n'),
	);

	console.log(`[sync] wrote snapshot (${wrapper.length} chars) and ${cssParts.length} CSS chunk(s).`);
}

main().catch((error) => {
	console.error(error);
	process.exit(1);
});
